board = [[' '] * 3 for i in range(3)]

board[0][0] = 'X'
board[1][1] = 'X'
board[2][2] = 'X'


win_coord = [
    [(0, 0), (0, 1), (0, 2)],
    [(1, 0), (1, 1), (1, 2)],
    [(2, 0), (2, 1), (2, 2)],
    [(0, 0), (1, 0), (2, 0)],
    [(0, 1), (1, 1), (2, 1)],
    [(0, 2), (1, 2), (2, 2)],
    [(0, 0), (1, 1), (2, 2)],
    [(0, 2), (1, 1), (2, 0)]
]


for coord in win_coord:
    a, b, c = coord[0], coord[1], coord[2]
    if board[a[0]][a[1]] == board[b[0]][b[1]] == board[c[0]][c[1]]:
        print('Win')

